int_num = 20
print(type(int_num))
int_num_f = float(int_num)
print(int_num_f)
print(type(int_num_f))
print('*'*20)

# float to int
float_num = 20.98
print(type(float_num))
float_num_i = int(float_num)
print(float_num_i)
print(type(float_num_i))
